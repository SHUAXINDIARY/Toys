const HomeCard = ({
    title,
    desc,
    url,
    openNewTag = true,
    status,
}: HomeCardProps) => {
    const [loading, setLoading] = useState(false);
    const isA = ["http", "https"].includes(url.split(":")[0]);
    if (status === STATUS.RENOVATED) {
        return (
            <div
                className="card w-64 h-36 m-10 bg-base-50 shadow-xl cursor-not-allowed tooltip"
                data-tip="装修中">
                <div className="card-body">
                    <h2 className="card-title">{title}</h2>
                    <p className="text-left">{desc}</p>
                </div>
            </div>
        );
    }
    if (isA) {
        return (
            <a
                className="cursor-pointer"
                href={url}
                target={openNewTag ? "_blank" : ""}
                rel="noreferrer">
                <div
                    className="card w-64 h-36 m-10 bg-base-100 shadow-xl hover:bg-base-200"
                    onClick={(_) => setLoading(true)}>
                    <div className="card-body">
                        <h2 className="card-title">{title}</h2>
                        <p>{desc}</p>
                    </div>
                </div>
            </a>
        );
    }
    return (
        <div className="relative cursor-pointer">
            {loading && (
                <Loading
                    type="spinningBubbles"
                    color="black"
                    className="absolute m-auto top-0 right-0 left-0 bottom-0 w-full h-ful"
                />
            )}
            <Link href={url} passHref>
                <div
                    className="card w-64 h-36 m-10 bg-base-100 shadow-xl hover:bg-base-200"
                    onClick={(_) => setLoading(true)}>
                    <div className="card-body">
                        <h2 className="card-title">{title}</h2>
                        <p>{desc}</p>
                    </div>
                </div>
            </Link>
        </div>
    );
};

